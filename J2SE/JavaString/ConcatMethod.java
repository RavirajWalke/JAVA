//Example of concat(String) method

class ConcatMethod{
 public static void main(String args[]){
 
   String s1="Sachin";
   String s2="Tendulkar";
   
   String s3="Mumbai".concat("Tendulkar");

   System.out.println(s3);
   
   //Concatination using + operator
   String s4="Nanded"+7+2;
   String s5="Nanded"+(7+2);
   System.out.println(s4);
   System.out.println(s5);

  }
}
